def display(df):
    """
    Attempts to use the shell's native rendering system to display a dataframe that
    expresses a TensorFlow transform.

    :param df: a Spark dataframe
    :return: nothing, the display is a side effect
    """
    if hasattr(df, 'tf_graph'):
        show_graph(df.tf_graph)
    else:
        print_schema(df)

